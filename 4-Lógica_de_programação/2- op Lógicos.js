/*
OPERADORES LÓGICOS

&& - and - E        tudo deve ser true para retornar true
|| - or  - ou
!  - not - Não

*/
console.log( true && true); // true
console.log( true && false);// false

console.log( true || true);//true
console.log( true || false);//true

console.log(!false) // true
console.log(!true) // false
console.log(!!true) // true (duas negações retorna ao valore original)

